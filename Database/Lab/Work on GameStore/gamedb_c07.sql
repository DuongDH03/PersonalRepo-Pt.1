-- Search function
CREATE OR REPLACE FUNCTION search_specify (something varchar)
returns table(
	game_id char(10),
	game_name varchar,
	dev_id char(10),
	dev_name varchar,
	rating numeric(2,1)
)
AS
$$
DECLARE 
	check_number_var boolean;
	check_asign_var boolean;
    string_search varchar;
BEGIN 
	select something ilike '#%' into check_number_var;
	select something ilike '@%' into check_asign_var;

	if check_asign_var then
	   SELECT SPLIT_PART( something , '@', 2) into string_search;
	   SELECT  quote_literal(string_search || '%') into string_search;
	   return QUERY EXECUTE 'SELECT  game_id, game_name, developer_id, dev_name, rating FROM game_small_details WHERE dev_name ilike  ' || string_search;
	end if;

	if check_number_var then
	   SELECT SPLIT_PART( something , '#', 2) into string_search;
	   SELECT  quote_literal(string_search || '%') into string_search;
	   return QUERY EXECUTE 'SELECT  game_id, game_name, developer_id, dev_name, rating FROM game_small_details JOIN belongs_to USING (game_id) JOIN genres USING (genre_id) WHERE genre_name ilike  ' || string_search;
	end if;

	SELECT quote_literal(something || '%') into string_search;
	RETURN QUERY EXECUTE 'SELECT  game_id, game_name, developer_id, dev_name, rating FROM game_small_details WHERE game_name ilike ' || string_search; 

END;
$$
LANGUAGE plpgsql;

CREATE OR REPLACE VIEW game_small_details AS
	(SELECT game_id, games.name as game_name, developer_id, game_dev.name as dev_name, rating 
	FROM games join game_dev using (developer_id) join game_rate using (game_id)
);

